-- Crear vista para departamentos
CREATE OR REPLACE VIEW public.colombia_states AS
SELECT id_state, state_name
FROM col.states
ORDER BY state_name;
-- Crear vista para ciudades
CREATE OR REPLACE VIEW public.colombia_cities AS
SELECT
    id_city,
    city_name,
    is_active,
    state_id
FROM col.cities
WHERE
    is_active = true
ORDER BY state_id, city_name;
